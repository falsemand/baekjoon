#include <bits/stdc++.h>

using namespace std;

long long n, m;

void solution()
{
    cin >> n >> m;

    if (n == m)
        cout << 1 << "\n";
    else
        cout << 0 << "\n";
}

int main(void)
{
    ios::sync_with_stdio(false);

    cin.tie(NULL);
    cout.tie(NULL);

    solution();

    return 0;
}