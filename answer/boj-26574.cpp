#include <bits/stdc++.h>

using namespace std;

int n, num;

void solution()
{
    cin >> n;

    while (n--) 
    {
        cin >> num;
        cout << num << " " << num << "\n";
    }
}

int main(void)
{
    ios::sync_with_stdio(false);

    cin.tie(NULL);
    cout.tie(NULL);

    solution();

    return 0;
}